    private void reconhecer() {

        //System.out.println(files[jComboBox1.getSelectedIndex()].getAbsolutePath());
        NeuralNetwork nnet = NeuralNetwork.load(files[jComboBox1.getSelectedIndex()].getAbsolutePath()); // load trained neural network saved with easyNeurons
       
        ImageRecognitionPlugin imageRecognition = (ImageRecognitionPlugin) nnet.getPlugin(ImageRecognitionPlugin.IMG_REC_PLUGIN_NAME); // get the image recognition plugin         from neural network

        ImageIcon Img = new ImageIcon(imagem);
        BufferedImage bi = amostraController.ImageIconBufferedImage(Img);
        HashMap<String, Double> output = imageRecognition.recognizeImage(bi);
        NumberFormat numberFormat = DecimalFormat.getNumberInstance();
        numberFormat.setMaximumFractionDigits(4);
        Iterator keys = output.keySet().iterator();
        List<Reconhecimento> reconhecimentos = new ArrayList<Reconhecimento>();
        Amostra amostra;
        Reconhecimento reconhecimento;
        TreeMap<Double, Reconhecimento> valores = new FastTreeMap<Double, Reconhecimento>();
        while (keys.hasNext()) {
            try {
                String key = (String) keys.next();
                amostra = amostraController.find(key.trim());
                reconhecimento = new Reconhecimento();
                reconhecimento.setAmostra(amostra);
                reconhecimento.setImagem(new ImageIcon(imagem));
                reconhecimento.setIndiceReconhecimento(numberFormat.format(output.get(key)));
                valores.put(output.get(key), reconhecimento);
            } catch (Exception ex) {
                Logger.getLogger(JFReconhecimento.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        reconhecimentos.addAll(valores.values());
        ordenado = new ArrayList<Reconhecimento>();
        for (int i = reconhecimentos.size() - 1; i >= 0; i--) {
            ordenado.add(reconhecimentos.get(i));
        }
        ReconhecimentoTabela rT = new ReconhecimentoTabela(ordenado);
        jTable1.setModel(rT);
        TableRowSorter ts = new TableRowSorter(jTable1.getModel());
        jTable1.setRowSorter(ts);
        jTable1.setRowHeight(55);

        jLabel3.setIcon(new ImageIcon(ordenado.get(0).getAmostra().getImagem().getImage().getScaledInstance(100, -1, 100)));

        double v = Double.parseDouble(ordenado.get(0).getIndiceReconhecimento().replace(",", "."));
        numberFormat.setMaximumFractionDigits(4);
        v = (v * 100);
        Reconhecimento reconhecimentoSelecionado = ordenado.get(0);
        jTextArea1.setText("Nome popular: " + ordenado.get(0).getAmostra().getDoenca().getDescricao().trim() + "\n");
        jTextArea1.append("Nome cientifico: " + ordenado.get(0).getAmostra().getDoenca().getNomecientifico().trim() + "\n");
        jTextArea1.append("C√≥digo amostra: " + ordenado.get(0).getAmostra().getIdamostra() + "\n");
        jTextArea1.append("Percentual de reconhecimento: " + numberFormat.format(v) + "%");
    }
