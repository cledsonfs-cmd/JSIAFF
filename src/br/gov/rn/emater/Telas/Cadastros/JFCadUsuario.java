    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String senha = "";
        char[] s = jPFSenha.getPassword();
        for (char c : s) {
            senha += c;
        }
        Usuario usuario = null;
        if (alteracao && usuarioAlteracao != null) {
            usuario = usuarioAlteracao;
        } else {
            usuario = new Usuario();
            usuario.setDatacadastro(new Timestamp(new Date().getTime()));
        }
        usuario.setLogin(jTFUsuario.getText());
        usuario.setSenha(senha);
        usuario.setAdministrador(jCBAcesso.getSelectedItem().toString().trim().equals("Administrador"));
        usuario.setOperador(jCBAcesso.getSelectedItem().toString().trim().equals("Operador"));
        usuario.setEspecialista(jCBAcesso.getSelectedItem().toString().trim().equals("Especialista"));
        usuario.setUsuario(jCBAcesso.getSelectedItem().toString().trim().equals("Usuario"));

        usuarioController.set(usuario);

        try {
            boolean retorno = usuarioController.salvar(alteracao);
            menssagem = usuarioController.getMenssagemRetorno();
            if (retorno) {
                JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Mensagem:.", JOptionPane.INFORMATION_MESSAGE);
                limpaTela();
                cargatabela();
            } else {
                JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Error:.", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(JFCadUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void limpaTela() {
        jTFUsuario.setEnabled(false);
        jPFSenha.setEnabled(false);
        jCBAcesso.setEnabled(false);
        jTFUsuario.setText("");
        jPFSenha.setText("");
        jCBAcesso.setSelectedIndex(0);
        alteracao = false;
        usuarioAlteracao = null;
        menssagem = "";
    }

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() > -1) {
            String login = (String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1);
            usuarioAlteracao = usuarioController.getPorLogin(login);
            if (usuarioAlteracao != null) {
                jTFUsuario.setText(usuarioAlteracao.getLogin());
                jPFSenha.setText(usuarioAlteracao.getSenha());
                if (usuarioAlteracao.isAdministrador()) {
                    jCBAcesso.setSelectedItem("Administrador");
                } else if (usuarioAlteracao.isOperador()) {
                    jCBAcesso.setSelectedItem("Operador");
                } else if (usuarioAlteracao.isEspecialista()) {
                    jCBAcesso.setSelectedItem("Especialista");
                } else if (usuarioAlteracao.isUsuario()) {
                    jCBAcesso.setSelectedItem("Usuário");
                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

