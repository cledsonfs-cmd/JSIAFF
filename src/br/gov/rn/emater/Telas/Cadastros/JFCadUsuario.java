    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTable1.getSelectedRow() > -1) {
            String login = (String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1);
            Usuario usuario = usuarioController.getPorLogin(login);

            int opc = JOptionPane.showConfirmDialog(this, "Confirma a exclusão do usuário " + usuario.getLogin(), ".:JSIAFF:Pergunta:.", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (opc == 0) {
                usuarioController.set(usuario);
                try {
                    boolean retorno = usuarioController.excluir();
                    menssagem = usuarioController.getMenssagemRetorno();
                    if (retorno) {
                        JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Mensagem:.", JOptionPane.INFORMATION_MESSAGE);
                        limpaTela();
                        cargatabela();
                    } else {
                        JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Error:.", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(JFCadUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

