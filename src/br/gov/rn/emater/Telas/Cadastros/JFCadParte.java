    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Parte parte;
        if (alterar && alterarFamilia != null) {
            parte = alterarFamilia;
        } else {
            parte = new Parte();
        }
        parte.setDescricao(jTFDescricao.getText().trim().toUpperCase());
        parte.setDatacadastro(new Timestamp(new Date().getTime()));
        parte.setIdUsuario(usuario.getIdUsuario());
        controller.set(parte);
        try {
            boolean retorno = controller.salvar(alterar);
            menssagem = controller.getMenssagemRetorno();
            if (retorno) {
                JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Mensagem:.", JOptionPane.INFORMATION_MESSAGE);
                limpaTela();
                cargatabela();
            } else {
                JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Error:.", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(JFCadGenero.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_jButton4ActionPerformed

