    private void jBImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBImprimirActionPerformed
        try {
            List<Parte> partes = controller.getTodos();
            parametros = new HashMap<String, String>();
            parametros.put("por", usuario.getLogin());
            new Imprimir(partes, "ModelosRelatorios/listaPartes.jasper", new ListaPartesDS((ArrayList) partes), parametros);
        } catch (Exception ex) {
            Logger.getLogger(JFCadParte.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBImprimirActionPerformed

    private void cargatabela() {
        jTParte.setModel(new ParteTabela());
        TableRowSorter ts = new TableRowSorter(jTParte.getModel());
        jTParte.setRowSorter(ts);
        jScrollPane1.setBorder(javax.swing.BorderFactory.createTitledBorder("Número de registros: " + jTParte.getModel().getRowCount()));
        jTParte.getColumnModel().getColumn(0).setMaxWidth(55);
        jTParte.getColumnModel().getColumn(2).setMaxWidth(65);
        jTParte.getColumnModel().getColumn(3).setMaxWidth(80);
    }
