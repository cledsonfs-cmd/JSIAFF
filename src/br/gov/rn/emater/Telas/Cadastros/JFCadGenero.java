    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Genero genero;
        if (alterar && alterarGenero != null) {
            genero = alterarGenero;
        } else {
            genero = new Genero();
        }
        genero.setDescricao(jTFDescricao.getText().trim().toUpperCase());
        genero.setDatacadastro(new Timestamp(new Date().getTime()));
        genero.setIdUsuario(usuario.getIdUsuario());
        genero.setIdFamilia(familias.get(jCBFamilas.getSelectedIndex()).getIdfamilia());
        controller.set(genero);
        try {
            boolean retorno = controller.salvar(alterar);
            menssagem = controller.getMenssagemRetorno();
            if (retorno) {
                JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Mensagem:.", JOptionPane.INFORMATION_MESSAGE);
                limpaTela();
                cargatabela();
            } else {
                JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Error:.", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(JFCadGenero.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_jButton4ActionPerformed

    private void limpaTela() {
        jTFDescricao.setText("");
        alterar = false;
        alterarGenero = null;
        jTFDescricao.setEnabled(false);
        menssagem = "";
    }

    private void cargatabela() {
        jTable1.setModel(new GeneroTabela());
        TableRowSorter ts = new TableRowSorter(jTable1.getModel());
        jTable1.setRowSorter(ts);
        jScrollPane1.setBorder(javax.swing.BorderFactory.createTitledBorder("Número de registros: " + jTable1.getModel().getRowCount()));
        jTable1.getColumnModel().getColumn(0).setMaxWidth(55);
        jTable1.getColumnModel().getColumn(3).setMaxWidth(65);
        jTable1.getColumnModel().getColumn(4).setMaxWidth(80);
    }
