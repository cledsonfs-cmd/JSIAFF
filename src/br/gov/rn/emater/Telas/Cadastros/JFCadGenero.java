    private void cargaLista() {
        Vector<String> nomes = new Vector<String>();
        try {
            familias = new FamiliaController().getTodos();
            for (Familia familia : familias) {
                nomes.add(familia.getDescricao().trim());
            }
        } catch (Exception ex) {
            Logger.getLogger(JFCadFamilia.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultComboBoxModel comboBoxModel = new DefaultComboBoxModel(nomes);
        jCBFamilas.setModel(comboBoxModel);
    }

