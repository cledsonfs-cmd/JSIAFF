    private void logar() {
        usuarioController = new UsuarioController();
        char[] x = jPFSenha.getPassword();
        String senha = "";
        for (char c : x) {
            senha += c;
        }
        boolean valida = usuarioController.validaUsuario(jTFUsuario.getText(), senha.trim());
        if (valida) {
            usuario = usuarioController.getPorLogin(jTFUsuario.getText());
            new JFPrincipal(usuario).setVisible(true);
            this.dispose();
        } else {
            String menssagem = usuarioController.getMenssagemRetorno();
            JOptionPane.showMessageDialog(this, menssagem, ".:JSIAFF:Error:.", JOptionPane.ERROR_MESSAGE);
        }
    }
